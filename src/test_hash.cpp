#include<hash.hpp>
#include<string>

int main(void)
{
std::string test_pattern("The quick brown fox jumps over the lazy dog");

std::sha512::value_type expected = {0x07, 0xe5, 0x47, 0xd9, 0x58, 0x6f, 0x6a, 0x73, 0xf7, 0x3f, 0xba, 0xc0, 0x43, 0x5e, 0xd7, 0x69, 0x51, 0x21, 0x8f, 0xb7, 0xd0, 0xc8, 0xd7, 0x88, 0xa3, 0x09, 0xd7, 0x85, 0x43, 0x6b, 0xbb, 0x64, 0x2e, 0x93, 0xa2, 0x52, 0xa9, 0x54, 0xf2, 0x39, 0x12, 0x54, 0x7d, 0x1e, 0x8a, 0x3b, 0x5e, 0xd6, 0xe1, 0xbf, 0xd7, 0x09, 0x78, 0x21, 0x23, 0x3f, 0xa0, 0x53, 0x8f, 0x3d, 0xb8, 0x54, 0xfe, 0xe6};

std::sha2<512> hash_fn;
hash_fn.process_bytes(test_pattern.begin(), test_pattern.end());
auto result = hash_fn.hash_value();

if(result != expected)
{
	return 1;
}

hash_fn.reset();
hash_fn.process_bytes(test_pattern.data(), test_pattern.size());
result = hash_fn.hash_value();

return result == expected?0:1;
}
